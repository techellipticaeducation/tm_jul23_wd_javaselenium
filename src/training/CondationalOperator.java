package training;

public class CondationalOperator {
	public static void main(String[] args) {
		//	Comparison - result - boolean

		int i = 10;
		int j = 11;

		System.out.println(i == j); // false 
		System.out.println(i != j); // true 
		System.out.println(i > j);  // false
		System.out.println(i >= j); // false
		System.out.println(i < j);  // true
		System.out.println(i <= j); // true





	}
}
